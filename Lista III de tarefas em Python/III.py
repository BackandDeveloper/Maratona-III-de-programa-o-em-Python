# Questão III:
R = float(input("Digite o valor do raio R da esfera: "))

# Cálculo do volume da esfera
volume = (4/3) * 3.14 * (R**3)

# Cálculo da área da superfície da esfera
area = 4 * 3.14 * (R**2)

# Saída dos resultados
print(f"Volume: {volume:.2f}")
print(f"Área: {area:.2f}")



